/**
 * main - entry point of the program
 * 
 * Return: 0 on succes and negative value if not
 */
int main()
{
  SDL_Event e;
  int quit = 0;

  if (init() < 0)
  {
    printf("Something fail in the init\n");
    return (-1);
  }
  else
  {
    while (!quit)
    {
      while (SDL_PollEvent(&e))
      {
        if (e.type == SDL_QUIT)
          quit = 1;
        handle_key_input();
        handle_mouse_input();
      }
      create_viewport();
      draw_map();
      cast_rays();

      /*Update the screen */
      SDL_RenderPresent(gRenderer);
    }
  }

  free_close();
  return (0);
}
